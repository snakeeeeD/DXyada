#pragma once
#include <vector>
#include <d3d11.h>
#include <DirectXMath.h>

//==============================
// リボンクラス
// ・放物線で飛ぶリボンの軌跡を管理
// ・DirectX11で線を描画する
//==============================
class Ribbon
{
public:
    //==============================
    // リボンの状態
    //==============================
    enum class State
    {
        Idle,       // 待機中
        Throwing,   // 投げられて飛んでいる
        Returning   // プレイヤー側へ戻っている
    };

private:
    //==============================
    // GPUに送る頂点構造体
    //==============================
    struct Vertex
    {
        float x, y, z;        // 頂点の座標
        float r, g, b, a;     // 頂点の色
        float u, v;
    };

    //==============================
    // リボンの軌跡（ライン上の点列）
    //==============================
    std::vector<DirectX::XMFLOAT2> m_points;

    //==============================
    // 投げ始め・狙い位置・速度
    //==============================
    DirectX::XMFLOAT2 m_playerPos{};  // プレイヤー追従用位置
    DirectX::XMFLOAT2 m_startPos{};   // 投げ始め位置
    DirectX::XMFLOAT2 m_targetPos{};  // 狙った位置
    DirectX::XMFLOAT2 m_velocity{};   // 初速ベクトル

    //==============================
    // 放物線用パラメータ
    //==============================
    float m_time = 0.0f;        // 経過時間
    float m_gravity = 2500.0f;  // 擬似重力加速度
    float m_speed = 1400.0f;    // 初速度の大きさ

    //==============================
    // 現在の状態
    //==============================
    State m_state = State::Idle;//初期は待機状態

    //==============================
    // DirectX用頂点バッファ
    //==============================
    ID3D11Buffer* m_vertexBuffer = nullptr;  // GPU側頂点バッファ
    int m_vertexCount = 0;                   // 現在の頂点数

public:
    //==============================
    // コンストラクタとデストラクタ
    //==============================
    Ribbon();
    ~Ribbon();

    //==============================
    // 初期化（頂点バッファの生成）
    //==============================
    void Init(ID3D11Device* device);

    //==============================
    // 更新処理（放物線計算など）
    //==============================
    void Update(float deltaTime);

    //==============================
    // プレイヤー位置をセット
    // Player::Update() から毎フレーム呼ぶ
    //==============================
    void SetPlayerPos(const DirectX::XMFLOAT2& pos);

    //==============================
    // リボンを投げる
    //==============================
    void Throw();

    //==============================
    // リボンを戻す
    //==============================
    void Return();

    //==============================
    // 状態をリセット
    //==============================
    void Reset();

    //==============================
    // 描画（ライン描画）
    //==============================
    void Draw(ID3D11DeviceContext* context);

    //==============================
    // 解放処理
    //==============================
    void UnInit();
};
